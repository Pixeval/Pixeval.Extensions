    private static IEnumerable<string> PythonPublicParameters(MethodDefinition method, bool skipTask)
    {
        var start = skipTask ? 1 : 0;
        for (var i = start; i < method.Parameters.Count; ++i)
        {
            if (TryMatchDictionaryExpansion(method, i, out var dictionary))
            {
                yield return $"{PythonIdentifier(ToSnakeCase(dictionary.Name))}: {PythonDictionaryAnnotation(dictionary)}";
                ++i;
                continue;
            }

            var parameter = method.Parameters[i];
            if (parameter.IsGeneratedArrayCount)
                continue;

            yield return $"{PythonIdentifier(ToSnakeCase(parameter.Name))}: {PythonSdkParameterAnnotation(method, parameter)}";
        }
    }

    private static IEnumerable<string> PythonCallArguments(MethodDefinition method, PidlMetadata metadata, bool skipTask)
    {
        var start = skipTask ? 1 : 0;
        for (var i = start; i < method.Parameters.Count; ++i)
        {
            if (TryMatchDictionaryExpansion(method, i, out var dictionary))
            {
                yield return PythonReadDictionaryArgument(dictionary);
                ++i;
                continue;
            }

            var parameter = method.Parameters[i];
            if (parameter.IsGeneratedArrayCount)
                continue;

            yield return PythonReadArgument(method, parameter, metadata);
        }
    }

    private static bool TryMatchDictionaryExpansion(MethodDefinition method, int index, out DictionaryExpansion dictionary)
    {
        dictionary = null!;
        if (index + 1 >= method.Parameters.Count)
            return false;

        var keys = method.Parameters[index];
        var values = method.Parameters[index + 1];
        var match = method.DictionaryParameters.FirstOrDefault(expansion =>
            expansion.KeysName == keys.Name &&
            expansion.ValuesName == values.Name);
        if (match is null)
            return false;

        dictionary = match;
        return true;
    }

    private static string PythonReadDictionaryArgument(DictionaryExpansion dictionary)
    {
        var keys = PythonReadDictionaryElement(dictionary.KeyType, dictionary.KeysName, dictionary.CountName);
        var values = PythonReadDictionaryElement(dictionary.ValueType, dictionary.ValuesName, dictionary.CountName);
        return $"dict(zip({keys}, {values}))";
    }

    private static string PythonReadDictionaryElement(string type, string name, string countName)
    {
        return type switch
        {
            PidlType.String => $"_abi.read_utf16_array({name}, {countName})",
            PidlType.IStream => $"[{PythonType.AbiStream}(pointer) for pointer in _abi.read_pointer_array({name}, {countName})]",
            PidlType.Int => $"_abi.read_int32_array({name}, {countName})",
            _ => $"_abi.read_pointer_array({name}, {countName})"
        };
    }

