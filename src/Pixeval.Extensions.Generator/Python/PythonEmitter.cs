    private static void AppendSdkPresetManifest(StringBuilder builder, PidlModel model)
    {
        var presets = model.Interfaces
            .Where(static definition => definition.SdkName is not null)
            .Select(static definition => definition.SdkName!)
            .Distinct(StringComparer.Ordinal)
            .Order(StringComparer.Ordinal)
