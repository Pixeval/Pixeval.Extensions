using System;
using System.Collections.Generic;
using System.Linq;
using System.Runtime.InteropServices;
using System.Text;
using Pixeval.Extensions.Generator.Models;

namespace Pixeval.Extensions.Generator;

internal static class CppEmitter
{
    private const string ExtensionSuffix = "Extension";
    private const string SettingsExtensionSuffix = "SettingsExtension";
    private const string ImageFormatProviderSuffix = "ImageFormatProvider";
    private const string TransformerCommandSuffix = "TransformerCommand";

    private static readonly Dictionary<string, SettingNameMapping> _SettingNameMappings = new(StringComparer.Ordinal)
    {
        ["Bool"] = new("boolean", "bool"),
        ["Int"] = new("integer", "int"),
        ["Double"] = new("floating", "double"),
        ["String"] = new("text", "string"),
        ["StringsArray"] = new("text_array", "strings_array"),
        ["Enum"] = new("enumeration", "int", "enum"),
        ["Color"] = new("color", "color"),
        ["DateTimeOffset"] = new("date_time_offset", "date_time_offset")
    };

    public static string EmitCommon(PidlModel model)
    {
        var context = new CppModelContext(model);
        var builder = new StringBuilder();
        _ = builder.AppendLine(
            """
            // <auto-generated />
            #pragma once
            #include <pixeval/detail/runtime.hpp>

            namespace pixeval::extensions
            {
            """);
        AppendAbiConfiguration(builder, model);
        AppendIids(builder, context);
        AppendEnums(builder, model);
        AppendSlots(builder, context);
        AppendSupportFunctions(builder, context);
        _ = builder.AppendLine("}");
        return builder.ToString();
    }

    public static string EmitSdk(PidlModel model)
    {
        var context = new CppModelContext(model);
        var builder = new StringBuilder();
        _ = builder.AppendLine(
            """
            // <auto-generated />
            #pragma once

            namespace pixeval::extensions
            {
            """);

        AppendSdkClasses(builder, context, model);
        _ = builder.AppendLine("namespace detail");
        _ = builder.AppendLine("{");
        AppendDefaultValues(builder, model);
        _ = builder.Append(EmitVtables(context));
        _ = builder.AppendLine("}");
        _ = builder.AppendLine("}");
        return builder.ToString();
    }

    private static void AppendSdkClasses(StringBuilder builder, CppModelContext context, PidlModel model)
    {
        foreach (var definition in OrderCppSdkDefinitions(context, model))
            AppendSdkClass(builder, context, definition);
    }

    private static IReadOnlyList<InterfaceDefinition> OrderCppSdkDefinitions(CppModelContext context, PidlModel model)
    {
        var definitions = model.Interfaces
            .Where(static definition => definition.SdkName is not null)
            .ToList();
        var pending = definitions.ToHashSet();
        var ordered = new List<InterfaceDefinition>();

        while (pending.Count > 0)
        {
            var added = false;
            foreach (var definition in pending.ToList())
            {
                var baseInterface = context.ResolveSdkBaseInterface(definition);
                if (baseInterface is not null && pending.Contains(baseInterface))
                    continue;

                ordered.Add(definition);
                _ = pending.Remove(definition);
                added = true;
            }

            if (!added)
                throw new InvalidOperationException("A cycle was found in C++ SDK base definitions.");
        }

        return ordered;
    }

    private static void AppendSdkClass(StringBuilder builder, CppModelContext context, InterfaceDefinition definition)
    {
        var className = definition.SdkName!;
        var baseClass = CppSdkBaseClass(context, definition);
        var methods = CppSdkMethods(context, definition).ToList();
        var properties = BuildCppSdkProperties(methods);
        var handled = new HashSet<MethodDefinition>();
        _ = builder.AppendLine($"class {className} : public {baseClass}");
        _ = builder.AppendLine("{");
        _ = builder.AppendLine("protected:");
        AppendSdkConstructor(builder, context, definition, className);
        _ = builder.AppendLine();
        _ = builder.AppendLine("public:");
        _ = builder.AppendLine($"    virtual ~{className}() = default;");

        if (IsConcreteSettingsInterface(context, definition, out var settingsType))
            _ = builder.AppendLine($"    [[nodiscard]] SettingsType settings_type() const override {{ return SettingsType::{settingsType}; }}");

        foreach (var method in methods)
        {
            if (handled.Contains(method.Method))
                continue;

            if (method.Method.PropertyAccessor is not PropertyAccessorKind.None)
            {
                var property = properties[method.Method.Property!.Name];
                AppendCppSdkProperty(builder, property);
                if (property.Getter is { } getter)
                    _ = handled.Add(getter.Method);
                if (property.Setter is { } setter)
                    _ = handled.Add(setter.Method);
                continue;
            }

            if (method.Method.IsAsyncResultGetter)
            {
                _ = handled.Add(method.Method);
                continue;
            }

            AppendCppSdkMethod(builder, method, FindAsyncResultGetter(methods, method.Method));
            _ = handled.Add(method.Method);
        }

        AppendCppSdkAsyncResultStorage(builder, methods);

        _ = builder.AppendLine("};");
        _ = builder.AppendLine();
    }

    private static string CppSdkBaseClass(CppModelContext context, InterfaceDefinition definition)
    {
        var shortName = ShortName(definition.FullName);
        if (shortName is "IExtension")
            return "ComExtensionBase";
        if (shortName is "ISettingsExtension")
            return "SettingBase";

        return context.ResolveSdkBaseInterface(definition)?.SdkName ?? "ComExtensionBase";
    }

    private static IEnumerable<MethodWithOwner> CppSdkMethods(CppModelContext context, InterfaceDefinition definition)
    {
        var shortName = ShortName(definition.FullName);
        if (shortName is "ISettingsExtension")
            return context.FlattenMethods(definition);

        var baseInterface = context.ResolveSdkBaseInterface(definition);
        var inherited = baseInterface is null
            ? []
            : context.FlattenInterfaces(baseInterface).ToHashSet();
        return context.FlattenMethods(definition).Where(method => !inherited.Contains(method.Owner));
    }

    private static void AppendSdkConstructor(StringBuilder builder, CppModelContext context, InterfaceDefinition definition, string className)
    {
        var shortName = ShortName(definition.FullName);
        if (shortName is "IExtension")
        {
            _ = builder.AppendLine($"    explicit {className}(detail::extension_kind kind) : ComExtensionBase(kind) {{}}");
            return;
        }

        if (shortName is "IEntryExtension" or "IFormatProviderExtension")
        {
            _ = builder.AppendLine($"    explicit {className}(detail::extension_kind kind) : {context.ResolveSdkBaseInterface(definition)!.SdkName}(kind) {{}}");
            return;
        }

        if (shortName is "ISettingsExtension")
        {
            _ = builder.AppendLine($"    explicit {className}(detail::setting_kind kind) : SettingBase(kind) {{}}");
            return;
        }

        if (IsConcreteSettingsInterface(context, definition, out var settingsType))
        {
            _ = builder.AppendLine($"    {className}() : {context.ResolveSdkBaseInterface(definition)!.SdkName}(detail::setting_kind::{SettingsKindName(definition)}) {{}}");
            return;
        }

        var baseInterface = context.ResolveSdkBaseInterface(definition);
        var kindName = ExtensionKindName(definition);
        if (baseInterface is null)
        {
            _ = builder.AppendLine($"    {className}() : ComExtensionBase(detail::extension_kind::{kindName}) {{}}");
        }
        else if (ShortName(baseInterface.FullName) is "IExtension")
        {
            _ = builder.AppendLine($"    explicit {className}(detail::extension_kind kind) : {baseInterface.SdkName}(kind) {{}}");
            _ = builder.AppendLine($"    {className}() : {className}(detail::extension_kind::{kindName}) {{}}");
        }
        else
        {
            _ = builder.AppendLine($"    explicit {className}(detail::extension_kind kind) : {baseInterface.SdkName}(kind) {{}}");
            _ = builder.AppendLine($"    {className}() : {className}(detail::extension_kind::{kindName}) {{}}");
        }
    }

    private static IReadOnlyDictionary<string, CppSdkProperty> BuildCppSdkProperties(IReadOnlyList<MethodWithOwner> methods)
    {
        var properties = new Dictionary<string, CppSdkProperty>(StringComparer.Ordinal);
        foreach (var method in methods.Where(static method => method.Method.PropertyAccessor is not PropertyAccessorKind.None))
        {
            var name = method.Method.Property!.Name;
            if (!properties.TryGetValue(name, out var property))
            {
                property = new CppSdkProperty(method.Method.Property!);
                properties.Add(name, property);
            }

            switch (method.Method.PropertyAccessor)
            {
                case PropertyAccessorKind.Getter:
                    property.Getter = method;
                    break;
                case PropertyAccessorKind.Setter:
                    property.Setter = method;
                    break;
            }
        }

        return properties;
    }

    private static void AppendCppSdkProperty(StringBuilder builder, CppSdkProperty property)
    {
        var name = ToSnakeCase(property.Name);
        var type = CppSdkPropertyType(property);
        if (property.DefaultValue is { } defaultValue)
        {
            if (property.Getter is null)
                throw new InvalidOperationException($"Property '{property.Name}' declares a default value without a getter.");

            _ = builder.AppendLine($"    [[nodiscard]] virtual {type} {name}() const {{ return {CppSdkDefaultValue(defaultValue, property.Getter.Method)}; }}");
        }
        else if (property.Getter is not null)
        {
            _ = builder.AppendLine($"    [[nodiscard]] virtual {type} {name}() const = 0;");
        }

        if (property.Setter is not null)
            _ = builder.AppendLine($"    virtual void {name}({type} value) = 0;");
    }

    private static void AppendCppSdkMethod(StringBuilder builder, MethodWithOwner method, MethodWithOwner? asyncResultGetter)
    {
        var returnType = CppSdkMethodReturnType(method.Method, asyncResultGetter?.Method);
        var name = ToSnakeCase(method.Method.Name);
        var parameters = string.Join(", ", CppSdkParameters(method.Method, skipTask: method.Method.IsAsync));
        var suffix = method.Method.IsVirtual ? $" {{ {CppSdkDefaultBody(method.Method, asyncResultGetter?.Method)} }}" : " = 0;";
        var nodiscard = returnType is "void" ? "" : "[[nodiscard]] ";
        _ = builder.AppendLine($"    {nodiscard}virtual {returnType} {name}({parameters}){suffix}");
    }

    private static void AppendCppSdkAsyncResultStorage(StringBuilder builder, IReadOnlyList<MethodWithOwner> methods)
    {
        var resultGetters = methods.Where(static method => method.Method.IsAsyncResultGetter).ToList();
        if (resultGetters.Count is 0)
            return;

        _ = builder.AppendLine();
        foreach (var resultGetter in resultGetters)
        {
            var type = CppSdkReturnType(resultGetter.Method);
            var stem = GetAsyncResultFieldStem(resultGetter.Method.Name);
            var fieldName = ToSnakeCase(stem) + "_";
            var accessorName = ToSnakeCase(stem) + "_value";
            var setterName = "set_" + ToSnakeCase(stem) + "_value";
            _ = builder.AppendLine($"    void {setterName}({type} value) {{ {fieldName} = std::move(value); }}");
            _ = builder.AppendLine($"    [[nodiscard]] const {type}& {accessorName}() const noexcept {{ return {fieldName}; }}");
            _ = builder.AppendLine($"    {type} {fieldName}{{}};");
        }
    }

    private static string CppSdkPropertyType(CppSdkProperty property)
    {
        if (property.Getter is { } getter)
            return CppSdkReturnType(getter.Method);

        return CppSdkSetterValueType(property.Setter!.Method);
    }

    private static string CppSdkSetterValueType(MethodDefinition method)
    {
        if (method.DictionaryParameters is [{ } dictionary])
            return CppSdkDictionaryType(dictionary);

        if (method.DateTimeOffsetParameters.Count is > 0)
            return "DateTimeOffsetValue";

        var value = method.Parameters.First(static parameter => !parameter.IsGeneratedArrayCount);
        return value.ArrayCountName is null ? CppSdkType(value.Type) : CppSdkArrayType(value.Type);
    }

    private static string CppSdkMethodReturnType(MethodDefinition method, MethodDefinition? asyncResultGetter)
    {
        if (method.IsAsync)
        {
            var resultType = asyncResultGetter is null ? "void" : CppSdkReturnType(asyncResultGetter);
            return resultType is "void" ? "task<void>" : $"task<{resultType}>";
        }

        return CppSdkReturnType(method);
    }

    private static string CppSdkReturnType(MethodDefinition method)
    {
        if (method.ReturnDictionary is { } dictionary)
            return CppSdkDictionaryType(dictionary);

        if (method.ReturnDateTimeOffset is not null)
            return "DateTimeOffsetValue";

        if (method.ReturnArrayCountName is not null)
            return CppSdkArrayType(method.ReturnType);

        return method.ReturnType is "void" ? "void" : CppSdkType(method.ReturnType);
    }

    private static IEnumerable<string> CppSdkParameters(MethodDefinition method, bool skipTask)
    {
        var start = skipTask ? 1 : 0;
        for (var i = start; i < method.Parameters.Count; ++i)
        {
            if (TryMatchDictionaryExpansion(method, i, out var dictionary))
            {
                yield return $"{CppSdkDictionaryType(dictionary)} {ToSnakeCase(dictionary.Name)}";
                ++i;
                continue;
            }

            if (TryMatchDateTimeOffsetExpansion(method, i, out var dateTimeOffset))
            {
                yield return $"DateTimeOffsetValue {ToSnakeCase(dateTimeOffset.Name)}";
                ++i;
                continue;
            }

            var parameter = method.Parameters[i];
            if (parameter.IsGeneratedArrayCount)
                continue;

            var type = parameter.ArrayCountName is null ? CppSdkType(parameter.Type) : CppSdkArrayType(parameter.Type);
            yield return $"{type} {ToSnakeCase(parameter.Name)}";
        }
    }

    private static string CppSdkDictionaryType(DictionaryExpansion dictionary)
    {
        return $"std::vector<std::pair<{CppSdkType(dictionary.KeyType)}, {CppSdkType(dictionary.ValueType)}>>";
    }

    private static string CppSdkArrayType(string type)
    {
        var elementType = ElementType(type);
        return elementType switch
        {
            "byte" => "std::vector<std::uint8_t>",
            _ => $"std::vector<{CppSdkType(elementType)}>"
        };
    }

    private static string CppSdkType(string type)
    {
        return type switch
        {
            "bool" => "bool",
            "byte" => "std::uint8_t",
            "short" => "std::int16_t",
            "ushort" => "std::uint16_t",
            "int" => "std::int32_t",
            "uint" => "std::uint32_t",
            "long" => "std::int64_t",
            "ulong" => "std::uint64_t",
            "double" => "double",
            "string" => "std::u16string",
            "string?" => "std::optional<std::u16string>",
            "IStream" => "Stream",
            "IProgressNotifier" => "ProgressNotifier",
            "ITaskCompletionSource" => "TaskCompletionSource",
            _ => ShortName(type)
        };
    }

    private static string CppSdkDefaultValue(string value, MethodDefinition method)
    {
        var literal = value.Trim();
        if (literal is "null")
            return "std::nullopt";
        if (literal is "default")
            return $"{CppSdkReturnType(method)}{{}}";

        return PidlDefaultValueFormatter.Cpp(literal, method.Property?.Type ?? method.ReturnType);
    }

    private static string CppSdkDefaultBody(MethodDefinition method, MethodDefinition? asyncResultGetter)
    {
        if (method.ReturnType is "void" && !method.IsAsync)
            return "";
        if (method.IsAsync)
        {
            if (asyncResultGetter is null)
                return "co_return;";

            return $"co_return {CppSdkImplicitDefaultValue(CppSdkReturnType(asyncResultGetter))};";
        }

        return $"return {CppSdkImplicitDefaultValue(CppSdkReturnType(method))};";
    }

    private static string CppSdkImplicitDefaultValue(string type)
    {
        return type switch
        {
            "void" => "",
            "bool" => "false",
            _ => $"{type}{{}}"
        };
    }

    private static MethodWithOwner? FindAsyncResultGetter(IReadOnlyList<MethodWithOwner> methods, MethodDefinition taskMethod)
    {
        return methods.FirstOrDefault(method => ReferenceEquals(method.Method.AsyncResultGetter?.SourceMethod, taskMethod));
    }

    private static string GetAsyncResultFieldStem(string methodName)
    {
        if (methodName.Length > 3 &&
            methodName.StartsWith("Get", StringComparison.Ordinal))
            return methodName[3..];

        return methodName;
    }

    private static bool TryMatchDateTimeOffsetExpansion(MethodDefinition method, int index, out DateTimeOffsetExpansion dateTimeOffset)
    {
        dateTimeOffset = null!;
        if (index + 1 >= method.Parameters.Count)
            return false;

        var ticks = method.Parameters[index];
        var offset = method.Parameters[index + 1];
        var match = method.DateTimeOffsetParameters.FirstOrDefault(expansion =>
            expansion.TicksName == ticks.Name &&
            expansion.OffsetName == offset.Name);
        if (match is null)
            return false;

        dateTimeOffset = match;
        return true;
    }

    private static bool TryMatchDictionaryExpansion(MethodDefinition method, int index, out DictionaryExpansion dictionary)
    {
        dictionary = null!;
        if (index + 1 >= method.Parameters.Count)
            return false;

        var keys = method.Parameters[index];
        var values = method.Parameters[index + 1];
        var match = method.DictionaryParameters.FirstOrDefault(expansion =>
            expansion.KeysName == keys.Name &&
            expansion.ValuesName == values.Name);
        if (match is null)
            return false;

        dictionary = match;
        return true;
    }

    private static void AppendDefaultValues(StringBuilder builder, PidlModel model)
    {
        var appended = false;
        foreach (var definition in model.Interfaces)
        {
            foreach (var property in definition.Properties.Where(static property => property.DefaultValue is not null))
            {
                var type = PidlDefaultValueFormatter.CppType(property.Type, property.DefaultValue!);
                var value = PidlDefaultValueFormatter.Cpp(property.DefaultValue!, property.Type);
                _ = builder.AppendLine($"inline constexpr {type} {DefaultValueConstantName(definition, property)} = {value};");
                appended = true;
            }
        }

        if (appended)
            _ = builder.AppendLine();
    }

    private static void AppendAbiConfiguration(StringBuilder builder, PidlModel model)
    {
        _ = builder.AppendLine(
            $$"""
            namespace abi
            {
                inline constexpr std::u16string_view sdk_version = u"{{model.SdkVersion}}";
                using bool_abi = {{CppBoolAbiType(model.Metadata)}};
                inline constexpr bool_abi bool_false = {{CppBoolFalse(model.Metadata)}};

                [[nodiscard]] inline bool bool_from_abi(bool_abi value) noexcept
                {
                    return {{CppBoolToCpp(model.Metadata, "value")}};
                }

                [[nodiscard]] inline bool_abi bool_to_abi(bool value) noexcept
                {
                    return {{CppBoolFromCpp(model.Metadata, "value")}};
                }
            }

            """);
    }

    private static string CppBoolAbiType(PidlMetadata metadata)
    {
        return metadata.BoolMarshalling switch
        {
            UnmanagedType.Bool => "std::int32_t",
            _ => throw new ArgumentOutOfRangeException(nameof(metadata))
        };
    }

    private static string CppBoolFalse(PidlMetadata metadata)
    {
        return metadata.BoolMarshalling switch
        {
            UnmanagedType.Bool => "0",
            _ => throw new ArgumentOutOfRangeException(nameof(metadata))
        };
    }

    private static string CppBoolFromCpp(PidlMetadata metadata, string expression)
    {
        return metadata.BoolMarshalling switch
        {
            UnmanagedType.Bool => $"({expression} ? 1 : 0)",
            _ => throw new ArgumentOutOfRangeException(nameof(metadata))
        };
    }

    private static string CppBoolToCpp(PidlMetadata metadata, string expression)
    {
        return metadata.BoolMarshalling switch
        {
            UnmanagedType.Bool => $"({expression} != 0)",
            _ => throw new ArgumentOutOfRangeException(nameof(metadata))
        };
    }

    private static string EmitVtables(CppModelContext context)
    {
        var builder = new StringBuilder();
        var hostVtable = context.HostVtable;
        var extensionVtables = context.ExtensionVtables;
        var settingVtables = context.SettingVtables;

        AppendVtable(builder, context, hostVtable);
        foreach (var vtable in extensionVtables)
            AppendVtable(builder, context, vtable);
        foreach (var vtable in settingVtables)
            AppendVtable(builder, context, vtable);

        AppendVtableFor(builder, "setting_kind", settingVtables, static vtable => vtable.SettingKind!);
        AppendVtableFor(builder, "extension_kind", extensionVtables, static vtable => vtable.ExtensionKind!);
        return builder.ToString();
    }

    private static void AppendIids(StringBuilder builder, CppModelContext context)
    {
        _ = builder.AppendLine(
            """
            namespace abi
            {
                constexpr guid iid_iunknown{0x00000000, 0x0000, 0x0000, {0xC0, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x46}};
            """);
        foreach (var definition in context.InterfacesWithGuid)
            _ = builder.AppendLine($"    constexpr guid iid_{AbiName(definition)}{FormatGuid(definition.Guid!)};");
        _ = builder.AppendLine(
            """
            }

            """);
    }

    private static void AppendEnums(StringBuilder builder, PidlModel model)
    {
        foreach (var definition in model.Enums)
        {
            _ = builder.AppendLine(
                $$"""
                enum class {{ShortName(definition.FullName)}} : std::int32_t
                {
                """);
            var nextValue = 0;
            foreach (var value in definition.Values)
            {
                var actualValue = value.Value ?? nextValue.ToString();
                _ = builder.AppendLine($"    {value.Name} = {actualValue},");
                if (int.TryParse(actualValue, out var parsed))
                    nextValue = parsed + 1;
                else
                    ++nextValue;
            }

            _ = builder.AppendLine(
                """
                };

                """);
        }
    }

    private static void AppendSlots(StringBuilder builder, CppModelContext context)
    {
        _ = builder.AppendLine(
            """
            namespace abi
            {
            """);
        foreach (var definition in context.InterfacesWithGuid)
        {
            _ = builder.AppendLine(
                $$"""
                    enum class {{AbiName(definition)}}_slot : std::int32_t
                    {
                        query_interface = 0,
                        add_ref = 1,
                        release = 2,
                """);

            var slot = 3;
            foreach (var method in context.FlattenMethods(definition))
                _ = builder.AppendLine($"        {ToSnakeCase(method.Method.Name)} = {slot++},");

            _ = builder.AppendLine(
                """
                    };

                """);
        }

        _ = builder.AppendLine(
            """
            }

            """);
    }

    private static void AppendSupportFunctions(StringBuilder builder, CppModelContext context)
    {
        _ = builder.AppendLine(
            """
            namespace abi
            {
            """);
        foreach (var definition in context.InterfacesWithGuid)
        {
            _ = builder.AppendLine(
                $$"""
                    [[nodiscard]] inline bool supports_{{AbiName(definition)}}(const guid& iid) noexcept
                    {
                """);

            var supported = context.FlattenInterfaces(definition)
                .Where(static value => value.Guid is not null)
                .Select(static value => $"iid_{AbiName(value)}")
                .ToList();

            _ = builder.AppendLine(
                "        return iid == iid_iunknown ||");
            for (var i = 0; i < supported.Count; i++)
            {
                var terminator = i == supported.Count - 1 ? ";" : " ||";
                _ = builder.AppendLine($"               iid == {supported[i]}{terminator}");
            }

            _ = builder.AppendLine(
                """
                    }

                """);
        }

        _ = builder.AppendLine(
            """
            }

            """);
    }

    private static void AppendVtable(StringBuilder builder, CppModelContext context, ConcreteVtable vtable)
    {
        var definition = context.RequireInterface(vtable.InterfaceName);
        _ = builder.AppendLine(
            $$"""
            inline void* {{vtable.Name}}[] = {
                reinterpret_cast<void*>(&{{ThunkPrefix(vtable)}}_query_interface),
                reinterpret_cast<void*>(&{{ThunkPrefix(vtable)}}_add_ref),
                reinterpret_cast<void*>(&{{ThunkPrefix(vtable)}}_release),
            """);

        foreach (var method in context.FlattenMethods(definition))
            _ = builder.AppendLine($"    reinterpret_cast<void*>(&{ThunkName(vtable, method)}),");

        TrimLastComma(builder);
        _ = builder.AppendLine(
            """
            };

            """);
    }

    private static void AppendVtableFor(
        StringBuilder builder,
        string kindType,
        IReadOnlyList<ConcreteVtable> vtables,
        Func<ConcreteVtable, string> kindSelector)
    {
        _ = builder.AppendLine(
            $$"""
            [[nodiscard]] inline void** vtable_for({{kindType}} kind) noexcept
            {
                switch (kind)
                {
            """);
        foreach (var vtable in vtables)
            _ = builder.AppendLine($"    case {kindSelector(vtable)}: return {vtable.Name};");
        _ = builder.AppendLine(
            $$"""
                }

                return {{vtables[0].Name}};
            }

            """);
    }

    private static string ThunkName(ConcreteVtable vtable, MethodWithOwner method)
    {
        if (vtable.SettingKind is not null)
            return SettingThunkName(vtable, method);
        if (vtable.ExtensionKind is not null)
            return ExtensionThunkName(method);

        return $"{ThunkPrefix(vtable)}_{ToSnakeCase(method.Method.Name)}";
    }

    private static string ExtensionThunkName(MethodWithOwner method)
    {
        var ownerStem = InterfaceStem(method.Owner.FullName, ExtensionSuffix);
        if (method.Method.Name is "FormatImage" &&
            TrimSuffix(ownerStem, ImageFormatProviderSuffix) is { Length: > 0 } imageKind)
            return $"extension_format_{ToSnakeCase(imageKind)}_image";
        if (method.Method.Name is "TransformAsync" &&
            TrimSuffix(ownerStem, TransformerCommandSuffix) is { Length: > 0 } transformKind)
            return $"extension_transform_{ToSnakeCase(transformKind)}";

        return $"extension_{ToSnakeCase(method.Method.Name)}";
    }

    private static string SettingThunkName(ConcreteVtable vtable, MethodWithOwner method)
    {
        var mapping = SettingNameMappingFor(vtable.InterfaceName);
        if (method.Method.PropertyAccessor is PropertyAccessorKind.Getter)
        {
            return method.Method.Property!.Name switch
            {
                "DefaultValue" => $"setting_get_{mapping.ValueThunkPrefix}_default",
                "MinValue" => $"setting_get_{mapping.ValueThunkPrefix}_min",
                "MaxValue" => $"setting_get_{mapping.ValueThunkPrefix}_max",
                "StepValue" => $"setting_get_{mapping.ValueThunkPrefix}_step",
                "EnumKeyValues" => $"setting_get_{mapping.SpecializedThunkPrefix}_key_values",
                _ => $"setting_get_{ToSnakeCase(method.Method.Property.Name)}"
            };
        }

        return method.Method.Name is "OnValueChanged"
            ? $"setting_on_{mapping.ValueThunkPrefix}_value_changed"
            : $"setting_{ToSnakeCase(method.Method.Name)}";
    }

    private static string ThunkPrefix(ConcreteVtable vtable)
    {
        if (vtable.InterfaceName is "IExtensionsHost")
            return "host";

        return vtable.SettingKind is null ? "extension" : "setting";
    }

    private static string FormatGuid(string value)
    {
        var parts = Guid.Parse(value).ToString("D").Split('-');
        var data4 = parts[3] + parts[4];
        var bytes = Enumerable.Range(0, 8)
            .Select(index => "0x" + data4.Substring(index * 2, 2).ToUpperInvariant());

        return $"{{0x{parts[0].ToUpperInvariant()}, 0x{parts[1].ToUpperInvariant()}, 0x{parts[2].ToUpperInvariant()}, {{{string.Join(", ", bytes)}}}}}";
    }

    private static string AbiName(InterfaceDefinition definition)
    {
        var name = ShortName(definition.FullName);
        if (name.StartsWith('I') && name.Length > 1 && char.IsUpper(name[1]))
            name = name[1..];

        return ToSnakeCase(name);
    }

    private static string DefaultValueConstantName(InterfaceDefinition definition, PropertyDefinition property)
    {
        return $"default_{AbiName(definition)}_{ToSnakeCase(property.Name)}";
    }

    private static string ToSnakeCase(string value)
    {
        var builder = new StringBuilder();
        for (var i = 0; i < value.Length; i++)
        {
            var current = value[i];
            if (char.IsUpper(current) && i > 0)
                _ = builder.Append('_');
            _ = builder.Append(char.ToLowerInvariant(current));
        }

        return builder.ToString();
    }

    private static SettingNameMapping SettingNameMappingFor(string interfaceName)
    {
        var stem = InterfaceStem(interfaceName, SettingsExtensionSuffix);
        return _SettingNameMappings.TryGetValue(stem, out var mapping)
            ? mapping
            : new SettingNameMapping(ToSnakeCase(stem), ToSnakeCase(stem));
    }

    private static string SettingsKindName(InterfaceDefinition definition)
    {
        return SettingNameMappingFor(definition.FullName).KindName;
    }

    private static string ExtensionKindName(InterfaceDefinition definition)
    {
        return ToSnakeCase(InterfaceStem(definition.FullName, ExtensionSuffix));
    }

    private static bool IsConcreteSettingsInterface(CppModelContext context, InterfaceDefinition definition, out string settingsType)
    {
        settingsType = "";
        if (ShortName(definition.FullName) is "ISettingsExtension" ||
            !context.FlattenInterfaces(definition).Any(static value => ShortName(value.FullName) is "ISettingsExtension"))
            return false;

        var shortName = ShortName(definition.FullName);
        if (!shortName.StartsWith('I') ||
            !shortName.EndsWith("SettingsExtension", StringComparison.Ordinal))
            return false;

        settingsType = shortName[1..^"SettingsExtension".Length];
        return settingsType.Length > 0;
    }

    private static string InterfaceStem(string interfaceName, string suffix)
    {
        var name = ShortName(interfaceName);
        if (name.StartsWith('I') && name.Length > 1 && char.IsUpper(name[1]))
            name = name[1..];
        if (name.EndsWith(suffix, StringComparison.Ordinal))
            name = name[..^suffix.Length];
        return name;
    }

    private static string? TrimSuffix(string value, string suffix)
    {
        return value.EndsWith(suffix, StringComparison.Ordinal) ? value[..^suffix.Length] : null;
    }

    private static string ElementType(string type)
    {
        if (type.EndsWith("[]?", StringComparison.Ordinal))
            return type[..^3];

        return type.EndsWith("[]", StringComparison.Ordinal) ? type[..^2] : type;
    }

    private static string ShortName(string fullName)
    {
        var separator = fullName.LastIndexOf('.');
        return separator < 0 ? fullName : fullName[(separator + 1)..];
    }

    private static void TrimLastComma(StringBuilder builder)
    {
        for (var i = builder.Length - 1; i >= 0; i--)
        {
            if (builder[i] is not ',')
                continue;

            _ = builder.Remove(i, 1);
            return;
        }
    }

    private sealed class CppModelContext
    {
        private readonly Dictionary<string, InterfaceDefinition> _interfaces;

        public CppModelContext(PidlModel model)
        {
            _interfaces = model.Interfaces
                .SelectMany(static definition => new[]
                {
                    new KeyValuePair<string, InterfaceDefinition>(definition.FullName, definition),
                    new KeyValuePair<string, InterfaceDefinition>(ShortName(definition.FullName), definition)
                })
                .GroupBy(static pair => pair.Key, StringComparer.Ordinal)
                .ToDictionary(static group => group.Key, static group => group.First().Value, StringComparer.Ordinal);

            InterfacesWithGuid = model.Interfaces.Where(static definition => definition.Guid is not null).ToList();
            HostVtable = new ConcreteVtable("host_vtable", "IExtensionsHost", null, null);
            SettingVtables = model.Interfaces
                .Where(definition => definition.SdkName is not null && !IsSdkAbstract(definition) && IsSettingsInterface(definition))
                .Select(definition => new ConcreteVtable(
                    $"{ToSnakeCase(SettingsKindName(definition))}_setting_vtable",
                    ShortName(definition.FullName),
                    null,
                    $"setting_kind::{SettingsKindName(definition)}"))
                .ToList();
            ExtensionVtables = model.Interfaces
                .Where(definition =>
                    definition.SdkName is not null &&
                    !IsSdkAbstract(definition) &&
                    ShortName(definition.FullName) is not "IExtension" &&
                    !IsSettingsInterface(definition))
                .Select(definition => new ConcreteVtable(
                    $"{ToSnakeCase(ExtensionKindName(definition))}_vtable",
                    ShortName(definition.FullName),
                    $"extension_kind::{ExtensionKindName(definition)}",
                    null))
                .ToList();
        }

        public IReadOnlyList<InterfaceDefinition> InterfacesWithGuid { get; }

        public ConcreteVtable HostVtable { get; }

        public IReadOnlyList<ConcreteVtable> ExtensionVtables { get; }

        public IReadOnlyList<ConcreteVtable> SettingVtables { get; }

        public InterfaceDefinition RequireInterface(string name)
        {
            if (_interfaces.TryGetValue(name, out var definition))
                return definition;

            throw new InvalidOperationException($"Interface '{name}' was not found in PIDL.");
        }

        public InterfaceDefinition? ResolveSdkBaseInterface(InterfaceDefinition definition)
        {
            if (definition.Inherits is null)
                return null;

            var current = RequireInterface(definition.Inherits);
            while (true)
            {
                if (current.SdkName is not null)
                    return current;

                if (current.Inherits is null)
                    return null;

                current = RequireInterface(current.Inherits);
            }
        }

        public IReadOnlyList<InterfaceDefinition> FlattenInterfaces(InterfaceDefinition definition)
        {
            var result = new List<InterfaceDefinition>();
            AppendInterfaceChain(definition, result);
            return result;
        }

        public IReadOnlyList<MethodWithOwner> FlattenMethods(InterfaceDefinition definition)
        {
            return FlattenInterfaces(definition)
                .SelectMany(static owner => owner.Methods.Select(method => new MethodWithOwner(owner, method)))
                .ToList();
        }

        private void AppendInterfaceChain(InterfaceDefinition definition, List<InterfaceDefinition> result)
        {
            if (definition.Inherits is not null)
                AppendInterfaceChain(RequireInterface(definition.Inherits), result);

            result.Add(definition);
        }

        private bool IsSettingsInterface(InterfaceDefinition definition)
        {
            return FlattenInterfaces(definition).Any(static value => ShortName(value.FullName) is "ISettingsExtension") &&
                   ShortName(definition.FullName) is not "ISettingsExtension";
        }

        private static bool IsSdkAbstract(InterfaceDefinition definition)
        {
            return definition.Attributes.Any(static attribute => attribute.Name is "sdkAbstract");
        }

        private static string SettingsKindName(InterfaceDefinition definition)
        {
            return SettingNameMappingFor(definition.FullName).KindName;
        }

        private static string ExtensionKindName(InterfaceDefinition definition)
        {
            return ToSnakeCase(InterfaceStem(definition.FullName, ExtensionSuffix));
        }
    }

    private sealed record ConcreteVtable(string Name, string InterfaceName, string? ExtensionKind, string? SettingKind);

    private sealed class CppSdkProperty(PropertyDefinition definition)
    {
        public string Name { get; } = definition.Name;

        public string? DefaultValue { get; } = definition.DefaultValue;

        public MethodWithOwner? Getter { get; set; }

        public MethodWithOwner? Setter { get; set; }
    }

    private sealed record SettingNameMapping(string KindName, string ValueThunkPrefix, string? SpecialThunkPrefix = null)
    {
        public string SpecializedThunkPrefix => SpecialThunkPrefix ?? ValueThunkPrefix;
    }

    private sealed record MethodWithOwner(InterfaceDefinition Owner, MethodDefinition Method);
}
